package User;

public class Adres {
	private String panstwo;
	private String miasto;
	private String kodpoczty;
	private String ulica;
	private String NrDomu;
	private String NrMieszkania;
	public String getPanstwo() {
		return panstwo;
	}
	public void setPanstwo(String panstwo) {
		this.panstwo = panstwo;
	}
	public String getMiasto() {
		return miasto;
	}
	public void setMiasto(String miasto) {
		this.miasto = miasto;
	}
	public String getKodpoczty() {
		return kodpoczty;
	}
	public void setKodpoczty(String kodpoczty) {
		this.kodpoczty = kodpoczty;
	}
	public String getUlica() {
		return ulica;
	}
	public void setUlica(String ulica) {
		this.ulica = ulica;
	}
	public String getNrDomu() {
		return NrDomu;
	}
	public void setNrDomu(String nrDomu) {
		NrDomu = nrDomu;
	}
	public String getNrMieszkania() {
		return NrMieszkania;
	}
	public void setNrMieszkania(String nrMieszkania) {
		NrMieszkania = nrMieszkania;
	}
	
}
